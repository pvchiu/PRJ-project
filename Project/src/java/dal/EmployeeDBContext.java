    @Override
    public ArrayList<Employee> list() {
        
        ArrayList<Employee> employee = new ArrayList<>();
        PreparedStatement stm = null;
        try {
            String sql = "SELECT e.eid, e.ename, e.phonenumber, e.[address], d.dname, s.salary from Employees e join Departments d on e.did = d.did\n"
                + "  join Salaries s on s.sid = e.sid ";
            stm = connection.prepareStatement(sql);
            ResultSet rs = stm.executeQuery();
            while (rs.next()) {
                Employee e = new Employee();
                e.setId(rs.getInt("eid"));
                e.setName(rs.getNString("ename"));
                e.setPhonenumber(rs.getNString("phonenumber"));
                e.setAddress(rs.getNString("address"));
                
                
                
                Department d = new Department();
                d.setName(rs.getNString("dname"));
                e.setDid(d);
                
                Salaries s = new Salaries();
                s.setSalary(rs.getDouble("salary"));
                e.setSid(s);
                
                employee.add(e);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ProductDBContext.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                stm.close();
                connection.close();
            } catch (SQLException ex) {
                Logger.getLogger(ProductDBContext.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return employee;
    }

